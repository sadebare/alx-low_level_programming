#include <stdio.h>

/**
 * add - functon that adds two integers
 *
 * Return: Addition of two integers
 */

int add(int a, int b)
{
    return (a+b);
}

/**
 * sub - functon that substract two integers
 *
 * Return: Substraction of two integers
 */

int sub(int a, int b)
{
    return (a-b);
}

/**
 * mul - functon that multiply two integers
 *
 * Return: Multiplication of two integers
 */

int mul(int a, int b)
{
    return (a*b);
}

/**
 * div - functon that divides two integers
 *
 * Return: Division of two integers
 */

int div(int a, int b)
{
    return (a/b);
}

/**
 * mod - functon that modulus two integers
 *
 * Return: modulus of two integers
 */

int mod(int a, int b)
{
    return (a%b);
}
